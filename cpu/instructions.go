package cpu
type Operation func(c *CPU) uint8
type AddrMode func(c *CPU)

type Instruction struct {
	Mnemonic      string
	Size          uint8
	AddrMode      AddrMode
	RegisterOne   Register
	RegisterTwo   Register
	RegisterOne16 Register16
	RegisterTwo16 Register16
	Operation     Operation
}

func (c *CPU) cpuCycles(cycles uint8) {
	var n int = int(cycles) * 4
	for i := 0; i < n; i++ {
		c.Ticks++
	}
	return
}

